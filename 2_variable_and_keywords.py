# In Python, a variable is a named reference to a value or an object. It allows you to store and manipulate data within your program. Unlike some other programming languages, Python does not require explicit declaration of variables. You can assign a value to a variable by using the assignment operator "=" and then use the variable throughout your code.

# Assigning a value to a variable
x = 5

# Accessing the value of a variable
print(x)  # Output: 5

# Modifying the value of a variable
x = 10

# Accessing the modified value
print(x)  # Output: 10

# Performing operations with variables
y = 3
z = x + y
print(z)  # Output: 13




